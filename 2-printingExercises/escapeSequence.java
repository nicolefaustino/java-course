package javaProgramming;
public class escapeSequence {

	public static void main(String[] args) {
		
		System.out.println("\bBackspace");
		System.out.println("\tTab");
		System.out.println("\nNew Line");
		System.out.println("Hello, World! \fForm Feed");
		System.out.println("\rCarriage Return");
		System.out.println("\"Double Quote");
		System.out.println("\'Single Quote");
		System.out.println("\\Backslash");

	}

}
